#ifndef _ICQTYPES_H_
#define _ICQTYPES_H_

#ifdef HAVE_CONFIG_H
#include <config.h>
#endif

#ifndef _WIN32
typedef unsigned long DWORD;
typedef unsigned short WORD;
typedef unsigned char BYTE;
typedef unsigned char BOOL;
#endif

typedef signed long S_DWORD;
typedef signed short S_WORD;
typedef signed char S_BYTE;

#endif /* _ICQTYPES_H_ */
