<h1>What is Pidgin?</h1>

<p>Pidgin is a multi-protocol Instant Messaging client that allows you to use all of your IM accounts at once.</p>

<p>Pidgin can work with:</p>

<ul class="condensed">
  <li>AIM</li>
  <li>ICQ</li>
  <li>SIMPLE</li>
  <li>Bonjour</li>
  <li>IRC</li>
  <li>Sametime</li>
  <li>Gadu-Gadu</li>
  <li>MSN</li>
  <li>XMPP</li>
  <li>Google Talk</li>
  <li>QQ</li>
  <li>Yahoo!</li>
  <li>Groupwise</li>
  <li>SILC</li>
  <li>Zephyr</li>
</ul>

