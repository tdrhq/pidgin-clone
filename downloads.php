<h2>PGP Signing</h2>

<p>
 All of the files we release are digitally signed.  What does this mean?  It 
 means that you can check the file against the digital signature and know 
 that the file hasn't been tampered with.
</p>

<p>
 To verify the signature of an RPM:
</p>
<ol>
 <li>
  Import the PGP key of the signer.  For Mandrake RPMs this is Mark Doliner.
  For Red Hat RPMs this is Ethan Blanton.  For Fedora Core this is Stu
  Tomlinson.  If you're using RPM 4.1.x or later:
  <pre><code>$ rpm --import "http://kingant.net/MarkDoliner.pgp"</code></pre>
  Or, if you're using 4.0.x:
  <pre><code>$ wget http://kingant.net/MarkDoliner.pgp
$ su -c 'gpg --import MarkDoliner.pgp'</code></pre>
 </li>

 <li>
  Verify the RPM.
  <pre><code>$ rpm -K gaim-1.3.0-0.mdk10.0.i586.rpm</code></pre>
 </li>
</ol>

<p>
 To verify the signature of a <code>.tar.gz</code> or <code>.tar.bz2</code> file:</p>
 <pre><code>$ gpg --verify gaim-0.80.tar.bz2.asc gaim-0.80.tar.bz2</code></pre>
</p>

